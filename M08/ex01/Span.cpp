# include "Span.hpp"

//====< constructor >===========================================================
Span::Span(void)
{
	v = new std::vector<int>;
}

//====< constructor >===========================================================
Span::Span(unsigned int N) : N(N)
{
	v = new std::vector<int>(N);
}

//====< copy constructor >======================================================
Span::Span(const Span & span)
{
	v = new std::vector<int>(span.N);
	*this = span;
}

//====< operator= >=============================================================
Span &	Span::operator=(const Span & span)
{
	for (int i = 0; i < N && i < span.N; i++)
		i += 0;
	return (*this);
}
